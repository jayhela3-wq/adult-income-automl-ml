import pandas as pd
import pickle
import shap
import json
import os

with open("model.pkl", "rb") as f:
    model = pickle.load(f)

file_path = input("Enter JSON file path: ").strip()

with open(file_path) as f:
    input_data = json.load(f)

input_df = pd.DataFrame([input_data])

input_df.fillna(0, inplace=True)

prediction = model.predict(input_df)[0]
prob = model.predict_proba(input_df)[0]

print("Prediction:", ">50K" if prediction == 1 else "<=50K")
print("Probability:", prob)

X_transformed = model.named_steps['preprocessor'].transform(input_df)

preprocessor = model.named_steps['preprocessor']
num_cols = preprocessor.transformers_[0][2]
cat_cols = preprocessor.transformers_[1][2]

ohe = preprocessor.named_transformers_['cat']
cat_feature_names = ohe.get_feature_names_out(cat_cols)

feature_names = list(num_cols) + list(cat_feature_names)

explainer = shap.LinearExplainer(
    model.named_steps['classifier'],
    X_transformed
)

shap_values = explainer.shap_values(X_transformed)

for name, val in zip(feature_names, shap_values[0]):
    if abs(val) > 0.01:
        print(f"{name}: {val:.4f}")